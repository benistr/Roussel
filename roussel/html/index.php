                Elle s'inscrira ensuite au Barreau de Nice en juillet 2021, au sein duquel elle exerce depuis dans un cabinet spécialisé en Droit de la famille, des personnes et de leur patrimoine. Riche de ses expériences internationales et professionnelles, toujours axées sur le droit de la famille, Maître Claire ROUSSEL apporte écoute, pragmatisme, sérieux, adaptabilité et réactivité dans les dossiers de cette matière particulièrement sensible et humaine.</p>
