                <h1 class="title-honoraires">Honoraires</h1>
                <p class="paragraphe-honoraires">Le montant des honoraires varie en fonction de la nature de la prestation, de la complexité de l'affaire et des enjeux du litige. Ils sont fixés individuellement pour chaque client.
                <br><br>
                Le premier rendez-vous, moment de rencontre, d'échange et d'élaboration d'une stratégie avec conseils pragmatiques prodigués, est facturé au taux horaires habituel, soit 200 euros.
                <br><br>
                Un forfait est ensuite proposé, en toute transparence, pour la suite du dossier, permettant au client d'avoir une prévisibilité globale du montant des honoraires.
                <br><br>
                Si des diligences complémentaires doivent être accomplies, en plus du forfait de base, elles sont discutées en amont avec le client, car facturées au temps passé.
                <br><br>
                Une convention d'honoraire est établie avec le client, résumant ces points.
                <br><br>
                Des facilités de paiement peuvent être mises en place, par le biais de paiements échelonnées.
                </p>
